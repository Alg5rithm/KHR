n = int(input())
dp = [[[0,0],[0,0]] for _ in range(100001)]

dp[0][1][0] = 1
dp[0][0][1] = 1
dp[0][0][0] = 1


for i in range(1, n+1) :
    dp[i][0][0] = (dp[i-1][0][1] + dp[i-1][0][0] + dp[i-1][1][0]) % 9901

    dp[i][0][1] = (dp[i-1][1][0] + dp[i-1][0][0]) % 9901

    dp[i][1][0] = (dp[i-1][0][1] + dp[i-1][0][0]) % 9901

sum1 = sum(dp[n-1][0]) % 9901
sum2 = sum(dp[n-1][1]) % 9901
print((sum1 + sum2) % 9901)
